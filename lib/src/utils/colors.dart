part of daynightbanner;

/*
 * The [PackageColor] class provides a collection of static color constants used in the daynightbanner package.
 * These colors are used to define the gradients for the day and night themes in the DayNightBanner widget.
 */
class PackageColor {
  /// Represents the first color gradient used between 7 AM and 8 AM.
  static const first7To8 = Color(0xFFFDFF9B);

  /// Represents the second color gradient used between 7 AM and 8 AM.
  static const second7To8 = Color(0xFFA0DFFF);

  /// Represents the white color used in the day and night themes.
  static const white = Colors.white;

  /// Represents the first color gradient used between 9 AM and 11 AM.
  static const first9To11 = Color(0xFFA0DFFF);

  /// Represents the first color gradient used between 12 PM and 3 PM.
  static const first12To15 = Color(0xFFA0DEFF);

  /// Represents the first color gradient used between 7 PM and 8 PM.
  static const first19To20 = Color(0xFFE3DC96);

  /// Represents the first color gradient used between 5 PM and 7 PM.
  static const first5To7 = Color(0xFF2B4EC8);

  /// Represents the first color gradient used at 4 PM.
  static const first16 = Color(0xFFA0DEFF);

  /// Represents the first color gradient used at 5 PM.
  static const first17 = Color(0xFFA0DFFF);

  /// Represents the first color gradient used at 6 PM.
  static const first18 = Color(0xFFFFF3C8);

  /// Represents the first color gradient used at 8 PM.
  static const first20 = Color(0xFF2B4EC8);

  /// Represents the first color gradient used at 5 AM.
  static const first5 = Color(0xFF2B4EC8);

  /// Represents the second color gradient used between 9 AM and 11 AM.
  static const second9To11 = Color(0xFFFFFEB3);

  /// Represents the second color gradient used between 12 PM and 3 PM.
  static const second12To15 = Color(0xFFD7DCFF);

  /// Represents the second color gradient used between 7 PM and 8 PM.
  static const second19To20 = Color(0xFF2B4EC8);

  /// Represents the second color gradient used between 5 PM and 7 PM.
  static const second5To7 = Color(0xFFE3DC96);

  /// Represents the second color gradient used at 4 PM.
  static const second16 = Color(0xFFD7DCFF);

  /// Represents the second color gradient used at 5 PM.
  static const second17 = Color(0xFFDDC263);

  /// Represents the second color gradient used at 6 PM.
  static const second18 = Color(0xFFD8B229);
}
